test('one triangle completely inside of another', function (t) {
  var A = [[0,0],[5,8],[10,0]]
  var B = [[1,1],[2,2],[3,1]]
  t.ok(peq(pclip.intersect(A,B,xyz), [
    [[[1,1],[2,2],[3,1]]],
  ]), 'intersect')
  t.ok(peq(pclip.exclude(A,B,xyz), [
    [
      [[0,0],[5,8],[10,0]],
      [[1,1],[2,2],[3,1]],
    ],
  ]), 'exclude')
  t.ok(peq(pclip.union(A,B,xyz), [
    [[[0,0],[5,8],[10,0]]]
  ]), 'union')
  t.ok(peq(pclip.difference(A,B,xyz), [
    [
      [[0,0],[5,8],[10,0]],
      [[1,1],[2,2],[3,1]],
    ],
  ]), 'difference')
  t.end()
})

